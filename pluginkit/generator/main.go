// generates the grpc_client for pluginkit
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/printer"
	"go/token"
	"os"
	"strings"
)

type param struct {
	Name string
	Type string
}

type method struct {
	Name    string
	Params  []param
	Results []string
}

var interfaceName = "FlixurPlugin"

func main() {
	pkgDir := flag.String("dir", ".", "directory containing the interface definition")
	flag.Parse()

	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(fset, *pkgDir, nil, parser.ParseComments)
	if err != nil {
		fmt.Fprintln(os.Stderr, "parse error:", err)
		os.Exit(1)
	}

	var (
		methods []method
		found   bool
	)

	for _, pkg := range pkgs {
		for _, file := range pkg.Files {
			for _, decl := range file.Decls {
				gd, ok := decl.(*ast.GenDecl)
				if !ok || gd.Tok != token.TYPE {
					continue
				}
				for _, spec := range gd.Specs {
					ts, ok := spec.(*ast.TypeSpec)
					if !ok || ts.Name.Name != interfaceName {
						continue
					}
					it, ok := ts.Type.(*ast.InterfaceType)
					if !ok {
						continue
					}
					found = true
					for _, m := range it.Methods.List {
						ft, ok := m.Type.(*ast.FuncType)
						if !ok || len(m.Names) == 0 {
							continue // skip embedded interfaces
						}
						methods = append(methods, extractMethod(fset, m.Names[0].Name, ft))
					}
				}
			}
		}
	}

	if !found {
		fmt.Fprintf(os.Stderr, "interface %s not found in %s\n", interfaceName, *pkgDir)
		os.Exit(1)
	}
	if len(methods) == 0 {
		fmt.Fprintf(os.Stderr, "interface %s has no methods\n", interfaceName)
		os.Exit(1)
	}

	genFile(true, methods)
	genFile(false, methods)
}

func genFile(genServer bool, methods []method) {
	src := generate(genServer, methods)

	formatted, err := format.Source([]byte(src))
	if err != nil {
		fmt.Fprintln(os.Stderr, "gofmt error:", err)
		fmt.Fprintln(os.Stderr, "--- generated source ---")
		fmt.Fprintln(os.Stderr, src)
		os.Exit(1)
	}

	outFile := "./grpc_client.go"
	if genServer {
		outFile = "./grpc_server.go"
	}

	if err := os.WriteFile(outFile, formatted, 0o644); err != nil {
		fmt.Fprintln(os.Stderr, "write error:", err)
		os.Exit(1)
	}

	fmt.Printf("wrote %s (%d methods from %s)\n", outFile, len(methods), interfaceName)
}

func extractMethod(fset *token.FileSet, name string, ft *ast.FuncType) method {
	m := method{Name: name}

	for _, f := range ft.Params.List {
		typeStr := exprString(fset, f.Type)
		if len(f.Names) == 0 {
			m.Params = append(m.Params, param{Name: "_", Type: typeStr})
			continue
		}
		for _, n := range f.Names {
			m.Params = append(m.Params, param{Name: n.Name, Type: typeStr})
		}
	}

	if ft.Results != nil {
		for _, r := range ft.Results.List {
			typeStr := exprString(fset, r.Type)
			count := len(r.Names)
			if count == 0 {
				count = 1
			}
			for i := 0; i < count; i++ {
				m.Results = append(m.Results, typeStr)
			}
		}
	}

	return m
}

func exprString(fset *token.FileSet, expr ast.Expr) string {
	var buf bytes.Buffer
	_ = printer.Fprint(&buf, fset, expr)
	return buf.String()
}

var sharedHeader = `// CODE GENERATED BY GO:GENERATE
package pluginkit

import (
	"context"

	pb "forge.xela.codes/xela/flixur/pluginkit/proto"
	"github.com/hashicorp/go-plugin"
	"google.golang.org/grpc"
)`
var serverHeader = `type grpcServer struct {
	pb.UnimplementedFlixurPluginServer
	Impl FlixurPlugin
}

func (p *FlixurGRPCPlugin) GRPCServer(broker *plugin.GRPCBroker, s *grpc.Server) error {
	pb.RegisterFlixurPluginServer(s, &grpcServer{Impl: p.Impl})
	return nil
}`
var clientHeader = `type grpcClient struct {
	client pb.FlixurPluginClient
}

func (p *FlixurGRPCPlugin) GRPCClient(ctx context.Context, broker *plugin.GRPCBroker, c *grpc.ClientConn) (interface{}, error) {
	return &grpcClient{client: pb.NewFlixurPluginClient(c)}, nil
}`

func generate(genServer bool, methods []method) string {
	var b strings.Builder

	fmt.Fprint(&b, sharedHeader+"\n\n")
	if genServer {
		fmt.Fprint(&b, serverHeader)
	} else {
		fmt.Fprint(&b, clientHeader)
	}
	fmt.Fprint(&b, "\n\n")

	for _, m := range methods {
		writeMethod(&b, genServer, m)
	}

	return b.String()
}

func writeMethod(b *strings.Builder, genServer bool, m method) {
	var paramParts []string
	for _, p := range m.Params {
		paramParts = append(paramParts, fmt.Sprintf("%s %s", p.Name, p.Type))
	}
	// the server needs the extra EmptyRequest param
	if genServer && len(paramParts) < 2 {
		paramParts = append(paramParts, "_ *pb.EmptyRequest")
	}

	resultList := ""
	switch len(m.Results) {
	case 0:
		// no return value
	case 1:
		resultList = m.Results[0]
	default:
		resultList = "(" + strings.Join(m.Results, ", ") + ")"
	}

	grpcName := "grpcClient"
	if genServer {
		grpcName = "grpcServer"
	}
	fmt.Fprintf(b, "func (m *%s) %s(%s) %s {\n", grpcName, m.Name, strings.Join(paramParts, ", "), resultList)

	var callArgs []string
	if len(m.Params) > 0 {
		// ctx is the first param
		callArgs = append(callArgs, m.Params[0].Name)
	}
	if len(m.Params) > 1 {
		callArgs = append(callArgs, m.Params[1].Name)
	} else if !genServer {
		// no request params gets an empty request from the client
		callArgs = append(callArgs, "&pb.EmptyRequest{}")
	}

	grpcName = "client"
	if genServer {
		grpcName = "Impl"
	}

	fmt.Fprintf(b, "\treturn m.%s.%s(%s)\n", grpcName, m.Name, strings.Join(callArgs, ", "))
	fmt.Fprintln(b, "}")
	fmt.Fprintln(b)
}
